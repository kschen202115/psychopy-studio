#!/usr/bin/env node
/**
 * Assemble the EdgeOne Pages Python cloud function (source / file-system routing).
 *
 * EdgeOne Pages routes by FILE PATH: a .py file under cloud-functions/ that
 * defines `class handler(BaseHTTPRequestHandler)` is auto-mapped to the URL of
 * its path. So cloud-functions/api/backend.py -> GET/POST /api/backend.
 * https://pages.edgeone.ai/document/cloud-functions
 *
 * Static frontend is handled separately (svelte:build -> dist/, declared as
 * outputDirectory in edgeone.json). This script only produces the function:
 *
 *   cloud-functions/
 *   ├── requirements.txt              (committed) pip deps, EdgeOne installs
 *   └── api/                          (gitignored, built here)
 *       ├── backend.py                copy of official_backend.py — defines
 *       │                             `class handler`, route = /api/backend
 *       ├── requirements.txt          same deps, also inside the function dir
 *       └── psychopy/                 pruned official PsychoPy source (git dev),
 *                                     a sibling so backend.py imports it
 *
 * PsychoPy source: $PSYCHOPY_CORE_SRC -> ../psychopy-core-src ->
 * ./psychopy-core-src -> shallow `git clone -b dev`.
 */
import { execFileSync } from "node:child_process";
import { cpSync, existsSync, mkdirSync, readdirSync, readFileSync, renameSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CF = join(ROOT, "cloud-functions");
const FUNC = join(CF, "api");                 // cloud-functions/api/backend.py -> /api/backend
const BACKEND_SRC = join(ROOT, "web_backend", "official_backend.py");
const REQUIREMENTS_SRC = join(CF, "requirements.txt");

// Trace-derived list of the only psychopy files the backend imports/reads
// (generated by exercising every command + a multi-component compile).
const KEEP_LIST = join(ROOT, "scripts", "psychopy-keep.txt");
const PSYCHOPY_GIT = "https://github.com/psychopy/psychopy.git";
const PSYCHOPY_BRANCH = "dev";

function findLocalSource() {
  const candidates = [
    process.env.PSYCHOPY_CORE_SRC && join(process.env.PSYCHOPY_CORE_SRC, "psychopy"),
    join(ROOT, "..", "psychopy-core-src", "psychopy"),
    join(ROOT, "psychopy-core-src", "psychopy"),
  ].filter(Boolean);
  return candidates.find((c) => existsSync(join(c, "experiment"))) || null;
}

function cloneSource() {
  const dest = join(tmpdir(), `psychopy-${PSYCHOPY_BRANCH}-${Date.now()}`);
  console.log(`[edgeone] cloning ${PSYCHOPY_GIT} (${PSYCHOPY_BRANCH}, --depth 1) -> ${dest}`);
  execFileSync("git", ["clone", "--depth", "1", "--branch", PSYCHOPY_BRANCH, PSYCHOPY_GIT, dest], { stdio: "inherit" });
  return join(dest, "psychopy");
}

function dirSizeMB(p) {
  try { return execFileSync("du", ["-sm", p], { encoding: "utf8" }).split(/\s+/)[0]; } catch { return "?"; }
}

if (!existsSync(BACKEND_SRC)) {
  console.error(`[edgeone] ERROR: backend not found at ${BACKEND_SRC}`);
  process.exit(1);
}

let src = findLocalSource();
if (src) console.log(`[edgeone] using local PsychoPy source: ${src}`);
else { console.log("[edgeone] no local PsychoPy checkout, cloning from git"); src = cloneSource(); }

console.log(`[edgeone] resetting ${FUNC}`);
rmSync(FUNC, { recursive: true, force: true });
mkdirSync(FUNC, { recursive: true });

// backend.py defines `class handler` and is self-contained (imports only the
// vendored psychopy sibling); its path cloud-functions/api/backend.py is the route.
console.log("[edgeone] handler: official_backend.py -> cloud-functions/api/backend.py");
cpSync(BACKEND_SRC, join(FUNC, "backend.py"));
if (existsSync(REQUIREMENTS_SRC)) cpSync(REQUIREMENTS_SRC, join(FUNC, "requirements.txt"));

// Copy ONLY the psychopy files the backend actually imports/reads, per the
// trace-derived keep-list (scripts/psychopy-keep.txt). This drops PsychoPy from
// ~64M to ~3M — everything not on a command path (GUI, hardware, demos, icons,
// unused submodules, etc.) is left out.
console.log("[edgeone] deps: copying backend-used psychopy files (keep-list)");
const psyDir = join(FUNC, "psychopy");
const keep = readFileSync(KEEP_LIST, "utf8").split("\n").map((l) => l.trim()).filter(Boolean);
let copied = 0;
const missing = [];
for (const rel of keep) {
  const s = join(src, rel);
  if (!existsSync(s)) { missing.push(rel); continue; }
  const d = join(psyDir, rel);
  mkdirSync(dirname(d), { recursive: true });
  cpSync(s, d);
  copied++;
}
console.log(`[edgeone]   kept ${copied} files` + (missing.length ? `, ${missing.length} listed-but-missing` : ""));
if (missing.length) console.warn("[edgeone]   missing:", missing.slice(0, 10).join(", "));

// preferences.py loads `<platform>.spec` (Darwin/Linux/Windows). The trace ran
// on macOS so the keep-list only has Darwin.spec; on EdgeOne (Linux) the missing
// Linux.spec makes prefs lack the [general] section (KeyError). The spec is
// effectively platform-agnostic, so reuse Darwin.spec for the other platforms.
// EdgeOne drops non-.py data files (incl. .spec) from the function bundle, so
// preferences.py's ConfigObj(<platform>.spec) reads an empty/missing file and
// prefs end up without the [general] section (KeyError). Embed the spec into a
// .py module, patch preferences.py to load it from there, and drop the .spec.
const prefDir = join(psyDir, "preferences");
const darwinSpec = join(prefDir, "Darwin.spec");
const prefsPy = join(prefDir, "preferences.py");
if (existsSync(darwinSpec) && existsSync(prefsPy)) {
  writeFileSync(
    join(prefDir, "_edgeone_spec.py"),
    `"""Embedded prefs spec — EdgeOne drops non-.py data files from the bundle."""\nSPEC = ${JSON.stringify(readFileSync(darwinSpec, "utf8"))}\n`,
  );
  const oldSpec = "        self.prefsSpec = ConfigObj(self.paths['prefsSpecFile'],\n                                   encoding='UTF8', list_values=False)";
  const newSpec = "        # EdgeOne drops .spec files; load the spec from the embedded copy.\n        from psychopy.preferences._edgeone_spec import SPEC as _SPEC\n        self.prefsSpec = ConfigObj(_SPEC.splitlines(), list_values=False)";
  const prefsSrc = readFileSync(prefsPy, "utf8");
  if (!prefsSrc.includes(oldSpec)) {
    console.error("[edgeone] ERROR: preferences spec-load line not found to patch");
    process.exit(1);
  }
  writeFileSync(prefsPy, prefsSrc.replace(oldSpec, newSpec));
  for (const f of ["Darwin.spec", "Linux.spec", "Windows.spec"]) rmSync(join(prefDir, f), { force: true });
  console.log("[edgeone] preferences: embedded spec -> _edgeone_spec.py, dropped .spec files");
}

// platform_specific/__init__.py does `from .linux import *` on Linux, but the
// macOS trace only kept darwin.py. Write a no-op linux.py (the real module is
// ctypes realtime-priority code the compile path never uses).
const platInit = join(psyDir, "platform_specific", "__init__.py");
if (existsSync(platInit)) {
  const linuxPy = join(psyDir, "platform_specific", "linux.py");
  writeFileSync(linuxPy, '"""Linux platform stub (serverless compile backend); real module is unused ctypes code."""\n\n\ndef rush(value=False, realtime=False):\n    return False\n');
  console.log("[edgeone] platform_specific: added no-op linux.py");
}

// EdgeOne's bundler drops dirs named "scripts" from the function, taking the
// PsychoJS/PsychoPy compiler (psychopy/scripts/psyexpCompile.py) with it.
// Rename it; backend.py imports compileScript with a scripts->pyscripts fallback.
const scriptsDir = join(psyDir, "scripts");
if (existsSync(scriptsDir)) {
  renameSync(scriptsDir, join(psyDir, "pyscripts"));
  console.log("[edgeone] renamed psychopy/scripts -> pyscripts (EdgeOne drops 'scripts')");
}

// EdgeOne's PythonFunctionBuilder rejects any file named like a stdlib module;
// psychopy ships psychopy/logging.py (clashes with `logging`). Rename it and
// install a lazy meta-path alias EARLY in psychopy/__init__.py so the ~148
// `import psychopy.logging` references resolve to the renamed file with the
// original (lazy) load order, avoiding the circular import a hard re-register
// would cause (preferences imports logging while __init__ is still running).
console.log("[edgeone] dodge stdlib clash: psychopy/logging.py -> _psylogging.py");
renameSync(join(psyDir, "logging.py"), join(psyDir, "_psylogging.py"));
const initPath = join(psyDir, "__init__.py");
const SHIM = `
# --- EdgeOne vendoring shim (added by scripts/build-edgeone-output.mjs):
# psychopy/logging.py was renamed to _psylogging.py (EdgeOne rejects files
# named like stdlib modules); alias it back lazily as psychopy.logging.
import os as _os
import importlib.abc as _ia, importlib.util as _iu
_PSY_DIR = _os.path.dirname(__file__)
class _PsyLoggingAlias(_ia.MetaPathFinder):
    def find_spec(self, name, path=None, target=None):
        if name == "psychopy.logging":
            return _iu.spec_from_file_location(
                "psychopy.logging", _os.path.join(_PSY_DIR, "_psylogging.py"))
        return None
sys.meta_path.insert(0, _PsyLoggingAlias())
`;
// EdgeOne's bundler drops extensionless files (VERSION, GIT_SHA), so
// psychopy/__init__.py's getVersion() FileNotFound-crashes at import. Hardcode
// the values (read from source) instead of reading those files at runtime.
const version = existsSync(join(src, "VERSION")) ? readFileSync(join(src, "VERSION"), "utf8").trim() : "0.0.0";
let gitSha = existsSync(join(src, "GIT_SHA")) ? readFileSync(join(src, "GIT_SHA"), "utf8").trim() : "";
// Use a non-"n/a" value so __init__ skips its `git rev-parse` subprocess fallback.
if (!gitSha || gitSha === "n/a") gitSha = "vendored";
let initSrc = readFileSync(initPath, "utf8");
initSrc = initSrc.replace(
  'return (pathlib.Path(__file__).parent/"VERSION").read_text(encoding="utf-8").strip()',
  `return ${JSON.stringify(version)}`,
);
initSrc = initSrc.replace(
  '__git_sha__ = (pathlib.Path(__file__).parent/"GIT_SHA").read_text(encoding="utf-8").strip()',
  `__git_sha__ = ${JSON.stringify(gitSha)}`,
);
const anchor = "import pathlib\n";
const at = initSrc.indexOf(anchor);
if (at === -1) {
  console.error("[edgeone] ERROR: insertion anchor not found in psychopy/__init__.py");
  process.exit(1);
}
initSrc = initSrc.slice(0, at + anchor.length) + SHIM + initSrc.slice(at + anchor.length);
writeFileSync(initPath, initSrc);
console.log(`[edgeone] hardcoded version ${version} / git_sha ${gitSha} (EdgeOne drops VERSION/GIT_SHA)`);

// EdgeOne's bundler keeps only .py files, dropping every data file PsychoPy
// reads at runtime (.tmpl templates, .yaml alerts, .svg icons, .json, …).
// Base64-embed them all so the function can restore them into a writable copy
// at startup (see _restore_psychopy_data in official_backend.py).
const dataFiles = {};
const walk = (dir, base) => {
  for (const ent of readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${ent.name}` : ent.name;
    if (ent.isDirectory()) walk(join(dir, ent.name), rel);
    else if (!ent.name.endsWith(".py")) dataFiles[rel] = readFileSync(join(dir, ent.name)).toString("base64");
  }
};
walk(psyDir, "");
const body = Object.entries(dataFiles).map(([k, v]) => `    ${JSON.stringify(k)}: ${JSON.stringify(v)},`).join("\n");
writeFileSync(
  join(psyDir, "_edgeone_data.py"),
  `"""Base64 of PsychoPy data files EdgeOne strips from the bundle; restored at runtime."""\nFILES = {\n${body}\n}\n`,
);
console.log(`[edgeone] embedded ${Object.keys(dataFiles).length} data files -> _edgeone_data.py`);

console.log(`[edgeone] done — cloud-functions/api is ${dirSizeMB(FUNC)}M (route /api/backend)`);
